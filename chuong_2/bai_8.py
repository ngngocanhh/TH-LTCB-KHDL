diem = input("Nhập điểm chữ của sinh viên: ")
if diem == 'A':
    print("Sinh viên xuất sắc")
elif diem == 'B':
    print("Sinh viên giỏi")
elif diem == 'C':
    print("Sinh viên khá")
elif diem == 'D':
    print("Sinh viên trung bình")
elif diem == 'E':
    print("Sinh viên yếu")
elif diem == 'F':
    print("Sinh viên khá")
else:
    print("Vui lòng nhập lại!")